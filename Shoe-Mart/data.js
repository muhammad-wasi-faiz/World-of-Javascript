const norm = (s) => (s || '').toLowerCase().replace(/\s+/g, ' ').trim();

import { getProductById } from './data.js';

// 1. Get the product ID from URL
const params = new URLSearchParams(window.location.search);
const productId = params.get('id');

// 2. Fetch the product from your data.js
const product = getProductById(productId);

if (product) {
  // 3. Update the title
  const titleEl = document.querySelector('[data-pv-title]');
  titleEl.textContent = product.title;

  // 4. Update the image
  const imgEl = document.querySelector('[data-pv-image]');
  if (product.images && product.images.length > 0) {
    imgEl.src = product.images[0];  // Use first image
  }

  // 5. Update price
  const priceEl = document.querySelector('[data-pv-price]');
  priceEl.textContent = `RS. ${product.price}`;

  const oldPriceWrap = document.querySelector('[data-pv-old-wrap]');
  const oldPriceEl = document.querySelector('[data-pv-old]');
  if (product.priceOriginal && product.priceOriginal > product.price) {
    oldPriceWrap.style.display = 'block';
    oldPriceEl.textContent = product.priceOriginal;
  }

  // 6. Update category
  const categoryEl = document.querySelector('[data-pv-category]');
  categoryEl.innerHTML = `<span class="font-normal">CATEGORIES</span> <span><i class="fa-solid fa-chevron-right"></i></span> ${product.category}`;
} else {
  console.error('Product not found!');
}


